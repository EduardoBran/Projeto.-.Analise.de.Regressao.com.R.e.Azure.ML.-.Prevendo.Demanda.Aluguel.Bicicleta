## Dicas para Storytelling

# - Não utilizar linguagem técnica em apresentações executivas.

# - Use apenas um ou dois gráficos para explicar seus resultados (use tabelas se necessário).

# - Tente construir uma linha do tempo, explicando como os dados geraram o resultado preditivo esperado.

# - Não use termos como "variáveis" e sim "atributos" ou "características".

# - Documente todo seu trabalho, pois isso servirá de apoio para que você possa contar a história dos dados.

# - Os tomadores de decisão não estão interessados em saber se você usou um algoritmo de regressão ou de classificação e nem
#   como você otimizou seu modelo. Eles querem saber o que precisam fazer para aumentar as vendas e o faturamento!

# - A fase de Análise Exploratóra dos Dados podem apresentar insights preciosos. Use isto a seu favor e guarde todos os resultados
#   intermediários do seu processo de análise!